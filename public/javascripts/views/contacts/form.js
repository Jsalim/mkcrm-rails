// Contact Table
$('#hide_contact_table').click(function() {
	$('#contact_editor_table').fadeToggle('slow', function() {
		return false;
	});	

	$('#hide_contact_table').toggle('slow', function() {
		$('#show_contact_table').toggle('slow', function() {
			return false;
		});	 
	});	
	
	$('#contact_editor_desc').fadeToggle('slow', function() {
		return false;
	});	
});



